package basics;

public class updatingVariables {
    public static void main(String[] args) {
        //updating of variable 
        int x = 5;  // x = 5
        System.out.println(x);
        x=7;    //x = 7
        System.out.println(x);
        x=x+6;    //increment  x=13
        System.out.println(x);
        x=x-20;    //decrement x = -7
        System.out.println(x);
        x = x * -1;  // x = 7
        System.out.println(x);
        x = x + 3;    // x = 10
        System.out.println(x);
        x = x / 2;   // x = 5
        System.out.println(x);
    }
    
}
