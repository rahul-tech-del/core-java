package basics;

public class compareTwoNumber {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 30;
        boolean isEqual = (num1 == num2);
        boolean isNotEqual = (num1 != num2);
        boolean isGreater = (num1 > num2);
        boolean isLess = (num1 < num2); 
        System.out.println("Are number equal :" +isEqual);
        System.out.println("Are number not equal :" +isNotEqual);
        System.out.println("Is the first number greater :" +isGreater);
        System.out.println("Are number not equal :" +isNotEqual);
        System.out.println("Is the first number less :" +isLess);

    }
    
}
