package basics;

public class performsArithmeticOperation {
    public static void main(String[] args) {
        int x = 20, y = 5;
        int add = x+y;
        int sub = add-10;
        System.out.println(sub);

        float f1 = 20.3f, f2 =8.0f;
        float mul = f1 *1;
        float div = mul / f2;
        System.out.println(div);

        double d1 = 50.0, d2 = 2.0;
        double div2 = d1 /d2;
        double Result = div2-0.5;
        System.out.println(Result);

        double finalDiv = 20.0 / 4.0;
        System.out.println(finalDiv);
    }
    
    
}
