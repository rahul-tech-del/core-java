package basics;

public class arithmaticOperationOnint {
    public static void main(String[] args) {
        int x = 5, y = 2;  // int is a data type and x is a variable
        System.out.println(x+y); 
        System.out.println(x-y); 
        System.out.println(x*y); 
        System.out.println(x/y); 
    }
    
}
